def _truncate(s: str, n: int) -> str:
    s = s or ""
    return s if len(s) <= n else s[:n - 1] + "…"


def serialize_session_state(
    question: str,
    session: SessionGraph,
    max_paths: int = 5,
    max_nodes: int = 15,
    max_edges: int = 10,
) -> str:
    """Compact state dump (kept for backward compat with existing tests)."""
    lines: List[str] = [
        f"Question: {_truncate(question, 200)}",
        f"Step: {session.step}",
        "",
    ]
    evid = [n for n in session.nodes.values() if n.node_type == NODE_EVIDENCE]
    notes = [n for n in session.nodes.values() if n.node_type == NODE_NOTE]
    concls = [n for n in session.nodes.values() if n.node_type == NODE_CONCLUSION]
    plan_steps = get_plan_steps(session)
    lines.append(f"Evidence: {len(evid)}  Notes: {len(notes)}  Conclusions: {len(concls)}  Plan steps: {len(plan_steps)}")

    active = sorted(
        [p for p in session.paths.values() if p.status == "active"],
        key=lambda p: -p.score,
    )
    lines.append(f"Active paths: {len(active)}")
    if active:
        lines.append("Best paths:")
        for p in active[:max_paths]:
            lines.append(f"  [{p.path_id}] ({p.path_type}, conf={p.confidence:.2f}): {_truncate(_format_path_text(p, session), 150)}")

    contradictions = find_contradictions_on_paths(session, active)
    if contradictions:
        lines.append(f"Contradictions on paths: {len(contradictions)}")
        for pair in contradictions[:3]:
            lines.append(f"  {pair[0]} <-> {pair[1]}")

    frontier_sorted = sorted(session.frontier, key=lambda x: -x.priority)
    if frontier_sorted:
        lines.append(f"Frontier ({len(frontier_sorted)}):")
        for f in frontier_sorted[:5]:
            nid = f.node_id or f.path_id or ""
            lines.append(f"  type={f.item_type} id={nid} reason=\"{f.reason}\" prio={f.priority:.2f}")

    if session.nodes:
        lines.append("Session nodes:")
        for nid in list(session.nodes.keys())[:max_nodes]:
            n = session.nodes[nid]
            lines.append(f"  {nid} [{n.node_type}] {_truncate(n.text, 60)}")
    if session.edges:
        lines.append("Edges:")
        for e in session.edges[:max_edges]:
            lines.append(f"  {e.src} --[{e.relation}]--> {e.dst} ({e.status})")
    return "\n".join(lines)


def build_briefing(
    question: str,
    session: SessionGraph,
    phase: Phase,
    step: int,
    max_steps: int,
    valid_actions: Set[ActionType],
    last_error: Optional[Dict[str, str]] = None,
    *,
    max_visible_nodes: int = 18,
    role_aware: bool = False,
) -> str:
    """Phase-aware state briefing handed to the model."""
    lines: List[str] = []
    lines.append(f"Phase: {phase.value}   step {step}/{max_steps}")
    lines.append(f"Question: {_truncate(question, 240)}")
    lines.append("")

    # Plan + coverage table
    cov = plan_step_coverage(session)
    if not cov:
        if phase == Phase.PLAN:
            lines.append("Plan: <empty — your job this step is to write it via PLAN>")
        else:
            lines.append("Plan: <missing — this should not happen; emit STOP>")
    else:
        lines.append("Plan:")
        focus = focus_plan_step(session)
        for row in cov:
            status = "DONE" if row["done"] else ("FOCUS" if focus and row["id"] == focus["id"] else "PENDING")
            lines.append(
                f"  [{row['id']}] \"{_truncate(row['text'], 80)}\"  "
                f"notes={row['notes']} conclusions={row['conclusions']} grounded={row['grounded_conclusions']}  {status}"
            )
    lines.append("")

    # Focus details
    focus = focus_plan_step(session)
    if focus is not None:
        ps_id = focus["id"]
        notes_here = nodes_for_plan_step(session, ps_id, NODE_NOTE)
        evidence_here = nodes_for_plan_step(session, ps_id, NODE_EVIDENCE)
        lines.append(f"Focus plan_step: {ps_id} \"{_truncate(focus['text'], 100)}\"")
        lines.append(f"  notes={focus['notes']}  conclusions={focus['conclusions']}  grounded={focus['grounded_conclusions']}")
        if notes_here or evidence_here:
            lines.append("  nodes already attached to this focus:")
            for n in (notes_here + evidence_here)[:6]:
                lines.append(f"    {n.id} [{n.node_type}] {_truncate(n.text, 80)}")
                # Show prior support_usage so the model can see HOW it
                # cited each support and stay consistent.
                usage = (n.metadata or {}).get("support_usage") if isinstance(n.metadata, dict) else None
                if usage:
                    for sup_id, used_txt in usage.items():
                        lines.append(f"        used from {sup_id}: {_truncate(used_txt, 80)}")
        lines.append("")

    # Signals
    active = [p for p in session.paths.values() if p.status == "active"]
    contradictions = find_contradictions_on_paths(session, active)
    # Direct contradict edges currently in the session (independent of path search)
    contradict_pairs = [
        (e.src, e.dst) for e in session.edges if e.relation == "contradict"
    ]
    if contradict_pairs:
        lines.append("CONTRADICT EDGES present in session. Cite ONLY the higher-confidence side, NEVER both. The lower-confidence node is typically the misconception:")
        for s, d in contradict_pairs[:5]:
            cs = float(getattr(session.nodes.get(s), "confidence", 0.5)) if s in session.nodes else 0.5
            cd = float(getattr(session.nodes.get(d), "confidence", 0.5)) if d in session.nodes else 0.5
            trust = s if cs >= cd else d
            lines.append(f"  {s} (conf={cs:.2f})  <-[contradict]->  {d} (conf={cd:.2f})   -> trust: {trust}")
        lines.append("")

    signals: List[str] = []
    if active:
        signals.append(f"active paths: {len(active)}")
    if contradictions:
        sample = ", ".join(f"{a}<->{b}" for a, b in contradictions[:2])
        signals.append(f"contradictions: {len(contradictions)} ({sample})")
    high_prio_frontier = sorted(session.frontier, key=lambda x: -x.priority)[:3]
    if high_prio_frontier:
        ftxt = ", ".join(
            f"{f.item_type}:{(f.node_id or f.path_id)}@{f.priority:.2f}"
            for f in high_prio_frontier
        )
        signals.append(f"top frontier: {ftxt}")
    if signals:
        lines.append("Signals: " + "  |  ".join(signals))
        lines.append("")

    # Candidate supports — only evidence/note/conclusion nodes are valid
    # supports, ranked by evidence_score (relevance, hub penalty,
    # contradiction penalty, misconception penalty). The plan_step ids are
    # listed separately so the model can use them as the `plan_step` arg
    # but NOT as supports.
    candidates = [
        n for n in session.nodes.values()
        if n.id != "Q0" and n.node_type in VALID_SUPPORT_TYPES
    ]

    # Stage 3 overlay: when enabled, add a focus/role/contradict-pair
    # bonus on top of the question-only base score so candidate ranking
    # adapts to whichever plan_step is currently in focus. The base
    # evidence_score is unchanged — overlay is purely additive and
    # returns 0.0 when focus is empty.
    if role_aware:
        from anchor_retrieval import stage3_overlay_score
        from roles import detect_plan_step_roles
        focus_now = focus_plan_step(session)
        focus_text = focus_now["text"] if focus_now else ""
        focus_roles = detect_plan_step_roles(focus_text) if focus_text else None
    else:
        focus_text = ""
        focus_roles = None

    def _ranked_score(n: SessionNode) -> float:
        base = evidence_score(session, n.id, question)
        if role_aware and focus_text:
            base += stage3_overlay_score(
                session, n.id,
                focus_text=focus_text,
                focus_roles=focus_roles,
            )
        return base

    candidates.sort(key=lambda n: -_ranked_score(n))

    # Surface misconception nodes as a separate WARNING block above the
    # candidate list so the model cannot miss the labeling.
    misconception_in_session = [n for n in candidates if is_misconception_node(n.id)]
    if misconception_in_session:
        lines.append(
            "WARNING: misconception nodes are present in the session. "
            "Their TEXT BODIES state the WRONG view (suffix `_false` = misconception). "
            "Do NOT cite their text as evidence for the truth. Cite them only to "
            "REFUTE the misconception, and pair with the correct counterpart."
        )
        for n in misconception_in_session[:6]:
            lines.append(f"  [MISCONCEPTION] {n.id}  -- WRONG claim: {_truncate(n.text, 90)}")
        lines.append("")

    if candidates:
        lines.append("Candidate supports (ranked by relevance; cite 1-4 of these):")
        for n in candidates[:max_visible_nodes]:
            s = evidence_score(session, n.id, question)
            tag = " [MISCONCEPTION]" if is_misconception_node(n.id) else ""
            lines.append(f"  {n.id} [{n.node_type}]{tag} score={s:+.2f}  {_truncate(n.text, 80)}")
        lines.append("")

    # Plan-step ids (NOT supports; only valid for the `plan_step` arg)
    plan_ids = [n.id for n in get_plan_steps(session)]
    if plan_ids:
        lines.append(f"Plan-step ids (for the `plan_step` arg, NOT for supports): {plan_ids}")
        lines.append("")

    # Frontier for EXPAND_NODE (only show when EXPAND_NODE is actually allowed)
    if phase == Phase.EXPLORE and ActionType.EXPAND_NODE in valid_actions:
        unexpanded_frontier = [
            f.node_id for f in session.frontier
            if f.item_type == "expand_node" and f.node_id and f.node_id in session.nodes
        ]
        if unexpanded_frontier:
            lines.append(f"Unexpanded frontier (for EXPAND_NODE only): {unexpanded_frontier[:12]}")
            lines.append("")

    # Writing pressure: tell the model explicitly when gathering is blocked
    if phase == Phase.EXPLORE and ActionType.EXPAND_NODE not in valid_actions:
        cov = plan_step_coverage(session)
        uncovered = [r for r in cov if not r["done"]]
        remaining = max_steps - step
        if uncovered:
            ps_ids = ", ".join(r["id"] for r in uncovered)
            lines.append(
                f"WRITE NOW: EXPAND_NODE is disabled this turn "
                f"(remaining={remaining}, uncovered plan_steps={len(uncovered)}: {ps_ids}). "
                f"Pick NOTE or CONCLUDE citing already-gathered evidence."
            )
            lines.append("")

    # Error feedback from previous step
    if last_error:
        lines.append(f"Previous action error: action={last_error.get('action')} reason={last_error.get('reason')}")
        lines.append("Fix the issue: provide valid supports / valid phase / non-empty args.")
        lines.append("")

    # Available actions
    valid_names = sorted(a.value for a in valid_actions)
    lines.append(f"Available actions this phase: {valid_names}")
    lines.append('Output JSON: {"thought_summary": "...", "action": "ACTION", "args": {...}, "reason": "..."}')
    return "\n".join(lines)


def build_pred_context(
    question: str,
    session: SessionGraph,
    focus_node_ids: Optional[List[str]] = None,
    mode: str = "explore",
) -> Dict[str, Any]:
    if not focus_node_ids:
        focus_node_ids = [nid for nid in list(session.nodes.keys())[:5]]
    focus_texts = {nid: session.nodes[nid].text[:100] for nid in focus_node_ids if nid in session.nodes}
    active = [p for p in session.paths.values() if p.status == "active"]
    return {
        "question": question[:300],
        "mode": mode,
        "focus_nodes": focus_node_ids,
        "focus_node_texts": focus_texts,
        "active_paths": [
            {"path_id": p.path_id, "type": p.path_type, "nodes": p.node_ids, "confidence": p.confidence}
            for p in active[:3]
        ],
        "uncertain_edges": [
            [e.src, e.dst, e.relation] for e in session.edges
            if e.status == "draft" and e.confidence < 0.6
        ],
        "step": session.step,
        "total_nodes": len(session.nodes),
        "total_edges": len(session.edges),
    }


