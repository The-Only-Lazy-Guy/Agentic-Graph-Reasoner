def compute_phase(step: int, max_steps: int) -> Phase:
    if step == 0:
        return Phase.PLAN
    if step >= max_steps - 1:
        return Phase.CLOSE
    return Phase.EXPLORE


def adaptive_max_steps(plan_size: int, *, hard_cap: int = 48, floor: int = 8) -> int:
    """Estimate per-question step budget from the model's plan size.

    Each plan_step typically needs:
      - 1 EXPLORE turn to gather/expand evidence
      - 1 EXPLORE turn to NOTE or CONCLUDE
    Plus 1 turn for PLAN and 1 for FINALIZE_ANSWER. Add slack of two extra
    turns per plan_step for retries / VERIFY_EDGE / etc. The extra +2 over
    earlier (was `max(2, plan_size)`) ensures the model has room for an
    explicit FINALIZE_ANSWER even at boundary plan sizes (plan_size=4
    previously got budget=14 with zero finalize headroom).

    Capped at `hard_cap` to prevent runaway on pathological plans; floored
    at `floor` so very tiny plans still get sensible headroom.

      plan_size  ->  budget
              2  ->  8
              3  ->  13
              4  ->  16
              5  ->  19
             10  ->  34
             20  ->  48 (capped)
    """
    needed = 2 * plan_size + 2
    slack = max(2, plan_size + 2)
    return min(hard_cap, max(floor, needed + slack))


def get_plan_steps(session: SessionGraph) -> List[SessionNode]:
    plan = [n for n in session.nodes.values() if n.node_type == NODE_PLAN_STEP]
    plan.sort(key=lambda n: n.metadata.get("order", 0))
    return plan


def _next_seq_id(session: SessionGraph, prefix: str) -> str:
    """Return the next sequential id for a given prefix (ps_3, n_2, c_1...)."""
    n = sum(1 for k in session.nodes if k.startswith(f"{prefix}_"))
    return f"{prefix}_{n + 1}"


def _outgoing(session: SessionGraph, src: str, relation: str) -> List[str]:
    canon = _canon(relation)
    return [e.dst for e in session.edges if e.src == src and e.relation == canon]


def _incoming(session: SessionGraph, dst: str, relation: str) -> List[str]:
    canon = _canon(relation)
    return [e.src for e in session.edges if e.dst == dst and e.relation == canon]


def _canon(relation: str) -> str:
    # Mirror canonical_relation alias collapsing for the relations we write
    return {
        "supports": "support",
        "contradicts": "contradict",
    }.get(relation, relation)


def nodes_for_plan_step(
    session: SessionGraph, plan_step_id: str, node_type: str,
) -> List[SessionNode]:
    """Return nodes of given type that are part_of plan_step_id."""
    srcs = _incoming(session, plan_step_id, REL_PART_OF)
    return [session.nodes[s] for s in srcs if s in session.nodes and session.nodes[s].node_type == node_type]


def has_evidence_grounding(
    session: SessionGraph, node_id: str, _visited: Optional[Set[str]] = None,
) -> bool:
    """True iff some transitive 'supports' / 'derived_from' ancestor is evidence."""
    if _visited is None:
        _visited = set()
    if node_id in _visited:
        return False
    _visited.add(node_id)
    node = session.nodes.get(node_id)
    if node is None:
        return False
    if node.node_type == NODE_EVIDENCE:
        return True
    parents = _incoming(session, node_id, REL_SUPPORTS) + _incoming(session, node_id, REL_DERIVED_FROM)
    for p in parents:
        if has_evidence_grounding(session, p, _visited):
            return True
    return False


def plan_step_coverage(session: SessionGraph) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    for ps in get_plan_steps(session):
        notes = nodes_for_plan_step(session, ps.id, NODE_NOTE)
        concls = nodes_for_plan_step(session, ps.id, NODE_CONCLUSION)
        grounded = [c for c in concls if has_evidence_grounding(session, c.id)]
        rows.append({
            "id": ps.id,
            "order": ps.metadata.get("order", 0),
            "text": ps.text,
            "notes": len(notes),
            "conclusions": len(concls),
            "grounded_conclusions": len(grounded),
            "done": len(grounded) >= 1,
        })
    return rows


def coverage_complete(session: SessionGraph) -> bool:
    cov = plan_step_coverage(session)
    return bool(cov) and all(row["done"] for row in cov)


def focus_plan_step(session: SessionGraph) -> Optional[Dict[str, Any]]:
    for row in plan_step_coverage(session):
        if not row["done"]:
            return row
    return None


def valid_actions_for_phase(
    phase: Phase,
    session: SessionGraph,
    *,
    step: int = 0,
    max_steps: int = MAX_STEPS,
    last_action_type: Optional[ActionType] = None,
    pred_available: bool = False,
) -> Set[ActionType]:
    """Phase set with three dynamic constraints layered on:

    1. FINALIZE_ANSWER is hidden in EXPLORE until every plan_step has a
       grounded conclusion.
    2. **Alternation**: if the previous action was EXPAND_NODE, drop
       EXPAND_NODE from this turn so the model is forced to write
       (NOTE / CONCLUDE / VERIFY_EDGE / CREATE_HYPOTHESIS).
    3. **Budget pressure**: when remaining steps fall to <= uncovered
       plan_steps + 1, drop EXPAND_NODE and PRED so every remaining step
       must write toward a conclusion.
    4. RUN_PRED_LOCAL is hidden when no pred_model was supplied.
    """
    valid = set(PHASE_VALID_ACTIONS[phase])

    # Coverage-complete short-circuit: if every plan_step has a grounded
    # conclusion, the only useful actions are finalizing the answer or
    # stopping. This prevents the model from piling more conclusions onto
    # plan_steps that are already DONE.
    if phase == Phase.EXPLORE and coverage_complete(session):
        return {ActionType.FINALIZE_ANSWER, ActionType.STOP}

    if phase == Phase.EXPLORE and not coverage_complete(session):
        valid.discard(ActionType.FINALIZE_ANSWER)

    if phase == Phase.EXPLORE and last_action_type == ActionType.EXPAND_NODE:
        valid.discard(ActionType.EXPAND_NODE)

    if phase == Phase.EXPLORE:
        cov = plan_step_coverage(session)
        uncovered = sum(1 for r in cov if not r["done"])
        # Reserve 1 step for FINALIZE_ANSWER + 1 per uncovered plan_step
        # for at least one CONCLUDE each. When the budget gets tight, no
        # more gathering — pure writing.
        remaining = max_steps - step
        if uncovered > 0 and remaining <= uncovered + 1:
            valid.discard(ActionType.EXPAND_NODE)
            valid.discard(ActionType.RUN_PRED_LOCAL)
            valid.discard(ActionType.CREATE_HYPOTHESIS)

        # If the focus plan_step already has at least one NOTE but zero
        # grounded conclusions, force the model to CONCLUDE instead of
        # piling more NOTEs onto the same step.
        focus = focus_plan_step(session)
        if focus is not None and focus["notes"] >= 1 and focus["grounded_conclusions"] == 0:
            valid.discard(ActionType.NOTE)
            valid.discard(ActionType.EXPAND_NODE)
            valid.discard(ActionType.CREATE_HYPOTHESIS)
            valid.discard(ActionType.RUN_PRED_LOCAL)

    if not pred_available:
        valid.discard(ActionType.RUN_PRED_LOCAL)

    return valid


