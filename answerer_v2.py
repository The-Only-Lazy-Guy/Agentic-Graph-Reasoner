# ---------------------------------------------------------------------------
# LlamaServerController — talks to a local llama.cpp server over HTTP.
#
# Why this and not in-process llama-cpp-python:
#   - model stays loaded between Python invocations (huge for iteration)
#   - survives Python crashes
#   - swap backends (llama.cpp, vLLM, ollama) without touching this code
#
# Start the server first (separate terminal):
#   llama-server -m cache/models/GLM-4.7-Flash-Q2_K.gguf \
#                -ngl 20 -c 4096 --host 127.0.0.1 --port 6767
#
# Hard rules: localhost only, no API key, no SSL — this controller will
# refuse to talk to non-loopback hosts.
# ---------------------------------------------------------------------------

DEFAULT_LLAMA_SERVER_URL = "http://127.0.0.1:6767"

# Pre-action shortcut threshold: skip the LLM call after a successful PLAN
# when the top-priority frontier item is strictly greater than #2. Anchor
# priorities come from lexical_overlap of question vs node text and sit in
# tight clusters (~0.05 spread for 8 topical anchors), so we only require
# a non-tie. Auto-expanding the top anchor is always reversible — the
# model can EXPAND_NODE the others on subsequent turns.
AUTO_EXPAND_MARGIN = 1e-6


@dataclass
class LlamaServerConfig:
    base_url: str = DEFAULT_LLAMA_SERVER_URL
    temperature: float = 0.3
    max_tokens: int = 512
    timeout: float = 240.0   # generous; Q2_K @ 20 GPU layers can be slow
    auto_actions: bool = True   # enable PLAN->EXPAND_NODE shortcut (Tier 1 v1)


class LlamaServerController(Controller):
    # Circuit breaker: if the model emits unparseable JSON this many times
    # in a row, abort the run with a RuntimeError. Prevents the runaway
    # loop we saw on modexp where each parse_failed turn cost a full
    # max_tokens generation, racking up 28000s before finally exhausting
    # the adaptive step budget.
    MAX_CONSECUTIVE_PARSE_FAILURES: int = 3

    def __init__(self, config: Optional[LlamaServerConfig] = None) -> None:
        self.config = config or LlamaServerConfig()
        self._checked = False
        self._conversation_history: List[Dict[str, Any]] = []
        self._expanded_nodes: Set[str] = set()
        self._last_action_result: str = ""
        self._last_action_type: Optional[ActionType] = None
        self._auto_action_count: int = 0  # counted across one query
        self._consecutive_parse_failures: int = 0
        self._guard_localhost(self.config.base_url)

    @staticmethod
    def _guard_localhost(url: str) -> None:
        """Refuse anything that isn't loopback. No accidental remote hits."""
        from urllib.parse import urlparse
        host = (urlparse(url).hostname or "").lower()
        if host not in ("127.0.0.1", "localhost", "::1"):
            raise ValueError(
                f"LlamaServerController refuses non-localhost base_url "
                f"({url!r}). Use 127.0.0.1 or localhost only."
            )

    @property
    def name(self) -> str:
        return f"LlamaServer({self.config.base_url})"

    def _build_system_prompt(self) -> str:
        tool_texts = [f"- {a.value}: {desc}" for a, desc in TOOL_DESCRIPTIONS.items()]
        return GLM_SYSTEM_PROMPT.format(tool_descriptions="\n".join(tool_texts))

    def _ensure_server_reachable(self) -> None:
        """Hit /health once per process to fail fast with a useful message."""
        if self._checked:
            return
        import urllib.request
        import urllib.error
        url = self.config.base_url.rstrip("/") + "/health"
        try:
            with urllib.request.urlopen(url, timeout=5) as resp:
                _ = resp.read(64)  # don't care what it returns; reachable is enough
            self._checked = True
        except (urllib.error.URLError, OSError) as e:
            raise RuntimeError(
                f"llama-server unreachable at {self.config.base_url!r}. "
                f"Start it with:\n"
                f"  llama-server -m cache/models/GLM-4.7-Flash-Q2_K.gguf "
                f"-ngl 20 -c 4096 --host 127.0.0.1 --port 6767\n"
                f"Underlying error: {e}"
            )

    def _post_chat(self, messages: List[Dict[str, Any]], schema: Dict[str, Any]) -> Dict[str, Any]:
        import urllib.request
        import urllib.error
        self._ensure_server_reachable()
        payload = json.dumps({
            "messages": messages,
            "temperature": self.config.temperature,
            "max_tokens": self.config.max_tokens,
            # OpenAI-compatible JSON-Schema grammar (llama.cpp converts to
            # GBNF under the hood). Same shape llama-cpp-python uses.
            "response_format": {"type": "json_object", "schema": schema},
            # GLM-4.7-Flash has reasoning/thinking mode enabled in its
            # chat template by default. With thinking on, the model emits
            # a <think>...</think> block BEFORE the answer, which eats
            # the entire max_tokens budget on every turn and starves the
            # JSON output. We need the structured action, not the trace,
            # so disable thinking explicitly.
            "chat_template_kwargs": {"enable_thinking": False},
            "reasoning_effort": "none",  # newer llama.cpp builds honor this too
            # Reuse prompt prefix across requests — much cheaper.
            "cache_prompt": True,
        }).encode("utf-8")
        url = self.config.base_url.rstrip("/") + "/v1/chat/completions"
        req = urllib.request.Request(
            url, data=payload,
            headers={"Content-Type": "application/json"},
            method="POST",
        )
        try:
            with urllib.request.urlopen(req, timeout=self.config.timeout) as resp:
                return json.loads(resp.read().decode("utf-8"))
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")[:500]
            raise RuntimeError(
                f"llama-server HTTP {e.code} on /v1/chat/completions: {body}"
            )

    def _try_auto_action(
        self,
        session: SessionGraph,
        phase: Phase,
        valid_actions: Set[ActionType],
        last_error: Optional[Dict[str, str]],
    ) -> Optional[Action]:
        """Return a deterministic Action when the next step is unambiguous,
        else None. Each successful return saves one LLM round-trip.

        v1 covers a single pattern: right after a successful PLAN, if the
        unexpanded frontier has a clear winner (priority margin >=
        AUTO_EXPAND_MARGIN over #2), auto-emit EXPAND_NODE on it. Skipped
        when the model just failed (let it adapt to its own error) or when
        EXPAND_NODE isn't in valid_actions for the current phase.
        """
        if not self.config.auto_actions:
            return None
        if last_error is not None:
            return None
        if self._last_action_type != ActionType.PLAN:
            return None
        if phase != Phase.EXPLORE:
            return None
        if ActionType.EXPAND_NODE not in valid_actions:
            return None

        candidates = [
            f for f in session.frontier
            if f.item_type == "expand_node"
            and f.node_id
            and f.node_id not in self._expanded_nodes
        ]
        if not candidates:
            return None
        candidates.sort(key=lambda f: -f.priority)
        top = candidates[0]
        second = candidates[1].priority if len(candidates) >= 2 else 0.0
        margin = top.priority - second
        if margin < AUTO_EXPAND_MARGIN:
            return None

        return Action(
            ActionType.EXPAND_NODE,
            {"node_id": top.node_id},
            f"auto: post-PLAN expand top frontier (priority={top.priority:.2f}, margin={margin:.2f})",
        )

    def choose_action(
        self,
        question: str,
        session: SessionGraph,
        step: int,
        max_steps: int,
        phase: Phase,
        valid_actions: Set[ActionType],
        last_error: Optional[Dict[str, str]] = None,
    ) -> Action:
        if step == 0:
            self._expanded_nodes.clear()
            self._conversation_history.clear()
            self._last_action_result = ""
            self._last_action_type = None
            self._auto_action_count = 0
            self._consecutive_parse_failures = 0

        # Tier-1 v1 chaining: skip the LLM call when the next action is
        # deterministically obvious. Currently covers ONE pattern, the
        # PLAN->EXPAND_NODE chain on the top-priority frontier item.
        # Returns None when conditions aren't met so we fall back to LLM.
        shortcut = self._try_auto_action(session, phase, valid_actions, last_error)
        if shortcut is not None:
            self._auto_action_count += 1
            return shortcut

        briefing = build_briefing(
            question, session, phase, step, max_steps, valid_actions, last_error,
            role_aware=getattr(session, "_role_aware", False),
        )

        messages: List[Dict[str, Any]] = [
            {"role": "system", "content": self._build_system_prompt()},
        ]
        # Keep the last turn only — Q2_K parrots prior outputs when given
        # more history. The briefing already carries all state we need.
        for msg in self._conversation_history[-2:]:
            messages.append(msg)
        messages.append({"role": "user", "content": briefing})

        # Narrow the action enum to ONLY the phase's valid actions so the
        # model is grammar-forbidden from emitting an out-of-phase choice.
        # Also constrain args.text minLength=1 (when present): NOTE and
        # CONCLUDE require text; PLAN/EXPAND_NODE/etc. simply omit it.
        # This eliminates the empty_text wasted-step failures we saw in
        # Stage A v2 traces without affecting actions that don't need text.
        turn_schema = dict(JSON_ACTION_SCHEMA)
        turn_schema["properties"] = dict(JSON_ACTION_SCHEMA["properties"])
        turn_schema["properties"]["action"] = {
            "type": "string",
            "enum": sorted(a.value for a in valid_actions) or [ActionType.STOP.value],
        }
        turn_schema["properties"]["args"] = {
            "type": "object",
            "properties": {
                "text": {"type": "string", "minLength": 1, "maxLength": 600},
                "supports": {
                    "type": "array",
                    "items": {"type": "string"},
                    "maxItems": SUPPORTS_MAX,
                },
            },
            "additionalProperties": True,
        }
        response = self._post_chat(messages, turn_schema)

        try:
            content = response["choices"][0]["message"]["content"].strip()
        except (KeyError, IndexError) as e:
            raise RuntimeError(f"unexpected llama-server response shape: {e}; got {response!r}")

        try:
            parsed = json.loads(content)
        except json.JSONDecodeError:
            self._consecutive_parse_failures += 1
            print(
                f"  [LlamaServer] JSON parse failed "
                f"(#{self._consecutive_parse_failures} consecutive); "
                f"raw[:200]={content[:200]!r}"
            )
            if self._consecutive_parse_failures >= self.MAX_CONSECUTIVE_PARSE_FAILURES:
                raise RuntimeError(
                    f"model stuck in parse-failure loop "
                    f"({self._consecutive_parse_failures} consecutive parse_failed). "
                    f"Last raw output[:200]={content[:200]!r}. "
                    f"This usually means args.text exceeded grammar maxLength or "
                    f"max_tokens={self.config.max_tokens} truncated the JSON mid-string."
                )
            self._conversation_history.append({"role": "assistant", "content": content})
            return Action(ActionType.STOP, {}, "parse_failed")

        # Successful parse — reset the failure counter.
        self._consecutive_parse_failures = 0

        action = Action.from_dict(parsed)

        # If the model picks an out-of-phase action, leave the validation to
        # the main loop — it will record the error and surface it on the next
        # turn. We DO repair invalid EXPAND_NODE node_ids because that's an
        # arg-shape issue, not a phase/protocol issue.
        if action.action_type == ActionType.EXPAND_NODE:
            nid = str(action.args.get("node_id", "")).strip()
            unexpanded = [
                f.node_id for f in session.frontier
                if f.item_type == "expand_node" and f.node_id and f.node_id not in self._expanded_nodes
            ]
            if nid not in unexpanded and unexpanded:
                action = Action(
                    ActionType.EXPAND_NODE,
                    {"node_id": unexpanded[0]},
                    f"corrected from {nid!r} to {unexpanded[0]!r} (not in frontier)",
                )

        self._conversation_history.append({"role": "assistant", "content": content})
        self._conversation_history.append({
            "role": "user",
            "content": json.dumps({"step": step, "action": action.action_type.value}),
        })
        return action

    def note_result(self, action: Action, result: ActionResult) -> None:
        if action.action_type == ActionType.EXPAND_NODE and result.success:
            nid = str(action.args.get("node_id", "")).strip()
            if nid:
                self._expanded_nodes.add(nid)
        if result.success:
            self._last_action_result = (
                f"{action.action_type.value} OK: +{result.new_nodes} nodes, "
                f"+{result.new_edges} edges, +{result.new_paths} paths"
            )
            self._last_action_type = action.action_type
        else:
            self._last_action_result = (
                f"{action.action_type.value} FAIL: {result.error} — {result.summary}"
            )
            # Leave _last_action_type untouched on failure so the chaining
            # gate keeps treating the last *successful* action as the trigger.
        # Feed the outcome back into the next turn so the model can adapt.
        self._conversation_history.append({
            "role": "user",
            "content": json.dumps({"step_result": self._last_action_result}),
        })


