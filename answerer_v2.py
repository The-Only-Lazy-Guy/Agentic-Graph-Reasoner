class Controller(ABC):
    @abstractmethod
    def choose_action(
        self,
        question: str,
        session: SessionGraph,
        step: int,
        max_steps: int,
        phase: Phase,
        valid_actions: Set[ActionType],
        last_error: Optional[Dict[str, str]] = None,
    ) -> Action:
        ...

    def note_result(self, action: Action, result: ActionResult) -> None:
        """Called after execute_action so the controller can adapt."""
        pass

    @property
    @abstractmethod
    def name(self) -> str:
        ...


# ---------------------------------------------------------------------------
# MockController — deterministic baseline that follows the new protocol
# ---------------------------------------------------------------------------

class MockController(Controller):
    def __init__(self) -> None:
        self._expanded_nodes: Set[str] = set()
        self._verified_edges: Set[Tuple[str, str]] = set()

    @property
    def name(self) -> str:
        return "MockController"

    def choose_action(
        self,
        question: str,
        session: SessionGraph,
        step: int,
        max_steps: int,
        phase: Phase,
        valid_actions: Set[ActionType],
        last_error: Optional[Dict[str, str]] = None,
    ) -> Action:
        if step == 0:
            self._expanded_nodes.clear()
            self._verified_edges.clear()

        if phase == Phase.PLAN:
            steps = self._default_plan(question)
            return Action(ActionType.PLAN, {"steps": steps}, "default 3-step plan")

        if phase == Phase.CLOSE:
            focus = focus_plan_step(session)
            if focus is not None:
                return self._best_conclude_for(session, focus["id"]) or Action(
                    ActionType.FINALIZE_ANSWER, {}, "no more progress possible"
                )
            return Action(ActionType.FINALIZE_ANSWER, {}, "coverage complete")

        # EXPLORE
        if coverage_complete(session):
            return Action(ActionType.FINALIZE_ANSWER, {}, "coverage complete")

        focus = focus_plan_step(session)
        focus_id = focus["id"] if focus else None

        # Conclude when we have at least one note for the focus
        if focus_id is not None:
            notes_here = nodes_for_plan_step(session, focus_id, NODE_NOTE)
            if notes_here:
                supports = [n.id for n in notes_here[:2]]
                text = "; ".join(_truncate(n.text, 80) for n in notes_here[:2])
                return Action(
                    ActionType.CONCLUDE,
                    {
                        "text": f"Based on notes: {text}",
                        "supports": supports,
                        "support_usage": self._stub_usage(session, supports),
                        "plan_step": focus_id,
                    },
                    f"concluding {focus_id} from gathered notes",
                )
            evid_here = nodes_for_plan_step(session, focus_id, NODE_EVIDENCE)
            if not evid_here:
                # Attach an evidence node from existing session evidence to the focus
                any_evid = [n for n in session.nodes.values() if n.node_type == NODE_EVIDENCE]
                if any_evid:
                    e = any_evid[0]
                    return Action(
                        ActionType.NOTE,
                        {
                            "text": _truncate(e.text, 140),
                            "supports": [e.id],
                            "support_usage": self._stub_usage(session, [e.id]),
                            "plan_step": focus_id,
                        },
                        f"first NOTE under {focus_id} citing {e.id}",
                    )

        # Expand an unexpanded frontier item
        frontier_expand = [
            f.node_id for f in sorted(session.frontier, key=lambda x: -x.priority)
            if f.item_type == "expand_node" and f.node_id and f.node_id not in self._expanded_nodes
        ]
        if frontier_expand:
            nid = frontier_expand[0]
            self._expanded_nodes.add(nid)
            return Action(ActionType.EXPAND_NODE, {"node_id": nid}, f"expanding {nid}")

        # If frontier is exhausted but coverage isn't complete, take a NOTE
        # over any evidence we already have
        any_evid = [n for n in session.nodes.values() if n.node_type == NODE_EVIDENCE]
        if any_evid and focus_id is not None:
            e = any_evid[0]
            return Action(
                ActionType.NOTE,
                {
                    "text": _truncate(e.text, 140),
                    "supports": [e.id],
                    "support_usage": self._stub_usage(session, [e.id]),
                    "plan_step": focus_id,
                },
                "fallback NOTE on existing evidence",
            )

        return Action(ActionType.FINALIZE_ANSWER, {}, "no further progress")

    @staticmethod
    def _stub_usage(session: SessionGraph, support_ids: List[str]) -> Dict[str, str]:
        """Mock's lazy usage map — copy each support's text as the usage
        string. Will pass the usage_coverage check trivially because
        cosine(text, text) = 1.0."""
        return {
            sid: _truncate(session.nodes[sid].text or "", 120)
            for sid in support_ids if sid in session.nodes
        }

    def _default_plan(self, question: str) -> List[str]:
        q = question.lower()
        if "compare" in q or " vs " in q or "difference" in q:
            return [
                "Identify each side of the comparison.",
                "Compare them on the key dimension.",
                "State the verdict.",
            ]
        if q.startswith("how ") or "how to" in q or "steps" in q:
            return [
                "List required preconditions.",
                "Describe the procedure step by step.",
                "Note common pitfalls.",
            ]
        return [
            "Identify the key concepts in the question.",
            "Gather supporting evidence from the graph.",
            "State the answer grounded in the evidence.",
        ]

    def _best_conclude_for(self, session: SessionGraph, plan_step_id: str) -> Optional[Action]:
        notes_here = nodes_for_plan_step(session, plan_step_id, NODE_NOTE)
        if notes_here:
            supports = [n.id for n in notes_here[:2]]
            text = "; ".join(_truncate(n.text, 80) for n in notes_here[:2])
            return Action(
                ActionType.CONCLUDE,
                {
                    "text": f"Based on notes: {text}",
                    "supports": supports,
                    "support_usage": self._stub_usage(session, supports),
                    "plan_step": plan_step_id,
                },
                "close-phase conclude",
            )
        any_evid = [n for n in session.nodes.values() if n.node_type == NODE_EVIDENCE]
        if any_evid:
            e = any_evid[0]
            return Action(
                ActionType.CONCLUDE,
                {
                    "text": _truncate(e.text, 140),
                    "supports": [e.id],
                    "support_usage": self._stub_usage(session, [e.id]),
                    "plan_step": plan_step_id,
                },
                "close-phase conclude direct from evidence",
            )
        return None


