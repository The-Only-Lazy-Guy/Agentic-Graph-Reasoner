def main() -> None:
    import argparse
    parser = argparse.ArgumentParser(
        description=(
            "Answerer-v2: graph-reasoning over a local GLM via llama-server. "
            "Start the server in a separate terminal before --controller server: "
            "llama-server -m cache/models/GLM-4.7-Flash-Q2_K.gguf -ngl 20 -c 4096 "
            "--host 127.0.0.1 --port 6767"
        )
    )
    parser.add_argument("--question", type=str, required=True)
    parser.add_argument("--graph", type=str, required=True, help="Path to graph JSON")
    parser.add_argument("--controller", type=str, default="mock", choices=["mock", "server"])
    parser.add_argument("--max-steps", type=int, default=MAX_STEPS,
                        help="0 = adaptive (auto-sized after PLAN by plan length); otherwise honored as the hard cap")
    parser.add_argument("--k-anchors", type=int, default=8)
    parser.add_argument("--server-url", default=DEFAULT_LLAMA_SERVER_URL,
                        help=f"llama-server base url (default {DEFAULT_LLAMA_SERVER_URL})")
    parser.add_argument("--temperature", type=float, default=0.3)
    parser.add_argument("--max-tokens", type=int, default=512)
    parser.add_argument("--output", type=str, default=None, help="Output JSON path")
    args = parser.parse_args()

    graph = MemoryGraph.load_json(args.graph)
    print(f"Loaded graph: {len(graph.nodes)} nodes, {len(graph.edges)} edges")
    print(f"Question: {args.question}")
    print(f"Controller: {args.controller}")

    if args.controller == "server":
        controller: Controller = LlamaServerController(LlamaServerConfig(
            base_url=args.server_url,
            temperature=args.temperature,
            max_tokens=args.max_tokens,
        ))
    else:
        controller = MockController()

    packet = answer_query_v2(
        question=args.question,
        graph=graph,
        controller=controller,
        max_steps=args.max_steps,
        k_anchors=args.k_anchors,
    )

    print("\n" + "=" * 60)
    print(f"Answer (type={packet.answer_type}, conf={packet.confidence:.3f})")
    print("=" * 60)
    print(packet.answer)
    print("=" * 60)
    print(f"Steps: {packet.steps_taken}, Paths: {packet.path_count}, Contradictions: {packet.contradictions_found}")

    if args.output:
        output = {
            "question": args.question,
            "answer": packet.answer,
            "confidence": packet.confidence,
            "answer_type": packet.answer_type,
            "steps_taken": packet.steps_taken,
            "path_count": packet.path_count,
            "contradictions_found": packet.contradictions_found,
            "evidence_paths": packet.evidence_paths,
        }
        Path(args.output).write_text(json.dumps(output, indent=2, ensure_ascii=False), encoding="utf-8")
        print(f"\nOutput saved to {args.output}")


if __name__ == "__main__":
    main()
