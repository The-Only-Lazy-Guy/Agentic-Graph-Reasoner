def _answer_query_v2_core(
    question: str,
    graph: MemoryGraph,
    controller: Controller,
    pred_model: Any,
    pred_device: Optional[torch.device],
    max_steps: int,
    k_anchors: int,
    *,
    anchor_strategy: str = "topk",
    graph_basename: str = "graph",
    role_aware: bool = False,
) -> Tuple[AnswerPacket, SessionGraph, List[Dict[str, Any]]]:
    session = SessionGraph(question)
    # Stash role_aware on the session so the controller's choose_action
    # can pass it to build_briefing without changing the Controller ABC
    # signature. Stage 3 opt-in.
    session._role_aware = role_aware  # type: ignore[attr-defined]
    if anchor_strategy == "legacy":
        anchors = retrieve_anchors_legacy(question, graph, k=k_anchors)
    else:
        anchors = retrieve_anchors_v2(
            question, graph, k=k_anchors,
            strategy=anchor_strategy, graph_basename=graph_basename,
        )
    session.import_memory_nodes(anchors, graph)
    # Pull in any main-graph edges between anchors so contradict/support
    # relations between them are visible before the first model turn.
    _import_missing_edges(session, graph)
    init_frontier_from_anchors(session, anchors)
    if pred_model is not None and pred_device is not None:
        memory_ids = [
            n.source_memory_id for n in session.nodes.values()
            if n.node_type == NODE_EVIDENCE and n.source_memory_id
        ][:8]
        run_pred_and_apply(session, pred_model, question, memory_ids, graph, pred_device)

    action_trace: List[Dict[str, Any]] = []
    last_error: Optional[Dict[str, str]] = None
    last_action_type: Optional[ActionType] = None
    finalized = False
    pred_available = pred_model is not None and pred_device is not None

    # Budget handling. max_steps == 0 is the sentinel for "adaptive":
    # start with a generous bound, then resize after PLAN based on the
    # model's actual plan size. Any other value is honored as-is so
    # callers retain full control (testing, hand-tuning, etc).
    adaptive_mode = (max_steps == 0)
    effective_max_steps = 24 if adaptive_mode else max_steps

    step = 0
    while step < effective_max_steps:
        session.step = step
        phase = compute_phase(step, effective_max_steps)
        valid = valid_actions_for_phase(
            phase, session,
            step=step, max_steps=effective_max_steps,
            last_action_type=last_action_type,
            pred_available=pred_available,
        )

        action = controller.choose_action(
            question, session, step, effective_max_steps, phase, valid, last_error,
        )
        entry: Dict[str, Any] = {
            "step": step,
            "phase": phase.value,
            "action": action.action_type.value,
            "args": action.args,
            "thought": action.thought_summary,
        }

        # Out-of-phase rejection
        if action.action_type not in valid:
            err = {
                "action": action.action_type.value,
                "reason": f"out_of_phase: {action.action_type.value} not valid in phase {phase.value} (allowed: {sorted(a.value for a in valid)})",
            }
            entry["result"] = {"success": False, "error": "out_of_phase", "summary": err["reason"]}
            last_error = err
            controller.note_result(action, ActionResult(action, False, summary=err["reason"], error="out_of_phase"))
            action_trace.append(entry)
            step += 1
            continue

        # Loop-handled actions
        if action.action_type == ActionType.FINALIZE_ANSWER:
            entry["result"] = {"success": True, "summary": "finalizing"}
            action_trace.append(entry)
            finalized = True
            break
        if action.action_type == ActionType.STOP:
            entry["result"] = {"success": True, "summary": "stop"}
            action_trace.append(entry)
            break

        # Execute via the dispatch table
        result = execute_action(action, session, graph, pred_model, pred_device)
        controller.note_result(action, result)
        entry["result"] = result.to_dict()
        action_trace.append(entry)
        last_error = None if result.success else {
            "action": action.action_type.value,
            "reason": f"{result.error}: {result.summary}",
        }
        last_action_type = action.action_type if result.success else last_action_type

        # Adaptive resize: once PLAN has succeeded, scale the budget to
        # match the plan size the model actually wrote. Only happens once,
        # and only when the caller asked for adaptive (max_steps=0).
        if (
            adaptive_mode
            and step == 0
            and action.action_type == ActionType.PLAN
            and result.success
        ):
            plan_size = len(get_plan_steps(session))
            if plan_size > 0:
                new_budget = adaptive_max_steps(plan_size)
                if new_budget != effective_max_steps:
                    effective_max_steps = new_budget
                    entry["budget_resized_to"] = new_budget  # surface in trace

        step += 1

    # Compose the answer deterministically from the graph
    answer_text = compose_graph_readout(session)

    _save_action_trace(question, action_trace, answer_text, finalized=finalized)

    # Build the AnswerPacket. Evidence paths are derived from session paths
    # for diagnostic continuity with v1; the answer itself comes from the
    # graph readout, not from the paths.
    all_paths = find_all_paths_from_question(session, max_depth=PATH_DEPTH_LIMIT)
    for p in all_paths:
        if p.path_id not in session.paths:
            session.add_path(p)
    all_paths = rank_paths(all_paths, question, session)
    contradictions = find_contradictions_on_paths(session, all_paths)

    cov = plan_step_coverage(session)
    confidence = (
        sum(row["done"] for row in cov) / len(cov) if cov else 0.0
    )

    evidence_data: List[Dict[str, Any]] = []
    for p in all_paths[:3]:
        evidence_data.append({
            "path_id": p.path_id,
            "type": p.path_type,
            "confidence": p.confidence,
            "score": p.score,
            "nodes": p.node_ids,
            "edges": [(s, d, r) for s, d, r in p.edges],
            "texts": p.evidence_texts,
        })

    packet = AnswerPacket(
        question=question,
        answer=answer_text,
        confidence=confidence,
        path_count=len(all_paths[:3]),
        evidence_paths=evidence_data,
        contradictions_found=len(contradictions),
        steps_taken=session.step,
        answer_type=classify_question_type(question),
    )
    return packet, session, action_trace


def _save_action_trace(
    question: str,
    trace: List[Dict[str, Any]],
    answer: str,
    *,
    finalized: bool = False,
) -> None:
    os.makedirs("artifacts", exist_ok=True)
    stamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S_%f")
    path = f"artifacts/trace_{stamp}_{uuid.uuid4().hex[:6]}.json"
    with open(path, "w", encoding="utf-8") as f:
        json.dump(
            {
                "question": question,
                "finalized": finalized,
                "trace": trace,
                "answer": answer[:1000],
            },
            f, ensure_ascii=False, indent=2,
        )


