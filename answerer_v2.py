from __future__ import annotations

"""
answerer_v2.py

Answerer-v2: phase-driven graph-reasoning controller.

Runs entirely on local GGUF weights via llama.cpp — no network APIs.

Core idea: the session graph IS the chain of thought.

  The controller's job each step is to WRITE a node or edge into the
  session graph. The final answer is composed deterministically from the
  graph (by walking plan_steps and their grounded conclusions in order),
  not free-generated by the LLM. Without the graph, there is no answer.

Phases:
  PLAN    (step 0)        emit PLAN with 2-4 short subgoals
  EXPLORE (steps 1..N-2)  alternate EXPAND_NODE (gather) and NOTE/CONCLUDE
                          (write) until every plan_step has >=1 grounded
                          conclusion
  CLOSE   (last steps)    fill any underexplored plan_step, then
                          FINALIZE_ANSWER

Hard rules enforced by execute_action:
  - NOTE / CONCLUDE require non-empty supports of valid session node ids.
  - The transitive support ancestry of every NOTE / CONCLUDE must include
    at least one evidence node (no free-floating claims).
  - Out-of-phase actions are rejected and the step is wasted (the error is
    fed back to the next prompt).
"""

