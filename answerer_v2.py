@dataclass
class Action:
    action_type: ActionType
    args: Dict[str, Any] = field(default_factory=dict)
    thought_summary: str = ""
    reason: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return {
            "action": self.action_type.value,
            "args": dict(self.args),
            "thought_summary": self.thought_summary,
            "reason": self.reason,
        }

    @staticmethod
    def from_dict(d: Dict[str, Any]) -> Action:
        atype_str = str(d.get("action", "STOP"))
        try:
            atype = ActionType(atype_str)
        except ValueError:
            atype = ActionType.STOP
        return Action(
            action_type=atype,
            args=dict(d.get("args", {})),
            thought_summary=str(d.get("thought_summary", "")),
            reason=str(d.get("reason", "")),
        )


