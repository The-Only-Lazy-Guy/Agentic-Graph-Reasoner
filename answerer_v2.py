# Detect code-bearing support nodes so their authoritative text is surfaced
# in the readout instead of being lost behind the model's paraphrase.
_CODE_DETECT_RE = re.compile(
    r"#include\s*[<\"]"
    r"|\bstd::"
    r"|\bint\s+main\s*\("
    r"|\bvector\s*<"
    r"|\btemplate\s*<"
    r"|\blong\s+long\s+\w+\s*\("
    r"|\bnullptr\b"
    r"|\bstruct\s+\w+\s*\{"
    r"|\bdef\s+\w+\s*\("
    r"|->\w"
)


def _node_contains_code(node: SessionNode) -> bool:
    """Detect code-bearing supports for the readout extension.

    Two-tier signal:
      1. Strong: id ends with `_apply` AND text contains both `{` and `;`.
         The `_apply` suffix is a graph-author convention that this node is
         meant to be code; combined with structural code markers (braces +
         semicolons) it generalizes across C/C++/Java/JS regardless of which
         std-lib headers happen to be present.
      2. Fallback: regex over explicit C/C++/Python tokens. Catches
         code-bearing nodes that don't follow the `_apply` suffix.
    """
    txt = node.text or ""
    if not txt:
        return False
    nid = (getattr(node, "id", "") or "").lower()
    if nid.endswith("_apply") and "{" in txt and ";" in txt:
        return True
    return bool(_CODE_DETECT_RE.search(txt))


def compose_graph_readout(session: SessionGraph) -> str:
    plan_steps = get_plan_steps(session)
    if not plan_steps:
        # No plan was ever written; degrade gracefully by listing any
        # grounded conclusions/notes that exist.
        concls = [n for n in session.nodes.values() if n.node_type == NODE_CONCLUSION]
        if not concls:
            notes = [n for n in session.nodes.values() if n.node_type == NODE_NOTE]
            if not notes:
                return "(no plan, no conclusions, no notes — the graph is empty.)"
            return "Notes only (no plan was written):\n" + "\n".join(
                f"  - {_truncate(n.text, 200)}" for n in notes
            )
        return "Conclusions only (no plan was written):\n" + "\n".join(
            f"  - {_truncate(c.text, 200)}" for c in concls
        )

    lines: List[str] = []
    seen_code: Set[str] = set()
    for ps in plan_steps:
        order = ps.metadata.get("order", 0)
        concls = nodes_for_plan_step(session, ps.id, NODE_CONCLUSION)
        grounded = [c for c in concls if has_evidence_grounding(session, c.id)]
        lines.append(f"{order + 1}. {ps.text}")
        if not grounded:
            if concls:
                lines.append("   (ungrounded conclusions only — underexplored)")
            else:
                lines.append("   (underexplored — no conclusions)")
            continue
        seen_text: Set[str] = set()
        for c in grounded:
            key = c.text.strip().lower()
            if key in seen_text:
                continue
            seen_text.add(key)
            supports = _incoming(session, c.id, REL_SUPPORTS)
            sup_str = f"  [supports: {', '.join(supports)}]" if supports else ""
            lines.append(f"   - {c.text}{sup_str}")
            for sup_id in supports:
                if sup_id in seen_code:
                    continue
                sup_node = session.nodes.get(sup_id)
                if sup_node and _node_contains_code(sup_node):
                    seen_code.add(sup_id)
                    lines.append("")
                    lines.append(f"     Source `{sup_id}`:")
                    lines.append("     ```")
                    for src_line in sup_node.text.splitlines():
                        lines.append(f"     {src_line}")
                    lines.append("     ```")
    return "\n".join(lines)


