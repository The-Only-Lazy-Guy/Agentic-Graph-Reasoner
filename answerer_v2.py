def _ok(action: Action, summary: str, **kw: Any) -> ActionResult:
    return ActionResult(action, True, summary=summary, **kw)


def _fail(action: Action, summary: str, error: str) -> ActionResult:
    return ActionResult(action, False, summary=summary, error=error)


SUPPORTS_MIN = 1
SUPPORTS_MAX = 4

# Node types that can be cited as a direct support. Plan steps organize
# reasoning but are not evidence; the question itself is not evidence;
# unverified hypotheses are not evidence-grade either.
VALID_SUPPORT_TYPES = {NODE_EVIDENCE, NODE_NOTE, NODE_CONCLUSION}


def _validate_supports(
    session: SessionGraph, supports: Iterable[str],
) -> Tuple[List[str], Optional[str]]:
    """Return (clean_ids, error_message_or_None).

    Rules enforced:
      - 1 to 4 ids
      - every id exists in the session
      - no Q0 (the question)
      - no plan_step / question / unverified hypothesis ids
      - no two supports may be linked by a contradict edge in either
        direction (the model must VERIFY_EDGE first)
    """
    ids = [str(s).strip() for s in supports if str(s).strip()]
    if len(ids) < SUPPORTS_MIN:
        return [], f"supports must contain at least {SUPPORTS_MIN} id"
    if len(ids) > SUPPORTS_MAX:
        # Suggest the top-SUPPORTS_MAX by score so the model can trim in
        # one retry instead of re-picking from scratch.
        q_text = session.nodes["Q0"].text if "Q0" in session.nodes else ""
        scorable = [s for s in ids if s in session.nodes]
        ranked = sorted(scorable, key=lambda s: -evidence_score(session, s, q_text))
        keep = ranked[:SUPPORTS_MAX]
        drop = [s for s in ids if s not in set(keep)]
        return ids, (
            f"too many supports ({len(ids)}); cap is {SUPPORTS_MAX}. "
            f"Keep top-{SUPPORTS_MAX} by relevance: {keep}. Drop: {drop}. "
            f"Reuse your previous text and support_usage entries for the kept ids."
        )

    missing = [s for s in ids if s not in session.nodes]
    if missing:
        return ids, f"unknown support ids: {missing}"

    if any(s == "Q0" for s in ids):
        return ids, "Q0 (the question) is not a valid support"

    invalid_types = [
        s for s in ids if session.nodes[s].node_type not in VALID_SUPPORT_TYPES
    ]
    if invalid_types:
        type_map = {s: session.nodes[s].node_type for s in invalid_types}
        return ids, (
            f"these ids cannot be direct supports: {type_map}. "
            "Only evidence / note / conclusion nodes count as supports — "
            "plan_step ids and hypotheses do not."
        )

    # Reject if any pair of supports is on opposite sides of a contradict
    # edge in the current session graph. The model must pick ONE side
    # (typically the higher-confidence node — see node.confidence) and
    # drop the other from the supports list. VERIFY_EDGE alone does NOT
    # resolve this — it only marks edge.status="verified" without
    # disqualifying either side, so retrying the same pair after a
    # verify still fails.
    for i, a in enumerate(ids):
        for b in ids[i + 1:]:
            if (
                session.edge_between(a, b) and session.edge_between(a, b).relation == "contradict"
            ) or (
                session.edge_between(b, a) and session.edge_between(b, a).relation == "contradict"
            ):
                # Suggest the higher-confidence side concretely.
                conf_a = float(getattr(session.nodes[a], "confidence", 0.5))
                conf_b = float(getattr(session.nodes[b], "confidence", 0.5))
                trusted = a if conf_a >= conf_b else b
                rejected = b if trusted == a else a
                return ids, (
                    f"{a} and {b} are linked by a contradict edge. Pick ONE side "
                    f"and drop the other. {trusted} (confidence {max(conf_a, conf_b):.2f}) "
                    f"is the higher-confidence side; {rejected} "
                    f"(confidence {min(conf_a, conf_b):.2f}) is likely the misconception. "
                    "Do not cite both."
                )

    return ids, None


def is_misconception_node(node_id: str) -> bool:
    """Convention: nodes whose id ends in `_false` carry text bodies that
    state the WRONG view. They are intended to be CITED-AS-MISCONCEPTIONS,
    not as evidence of truth. Naming convention is set by the graph
    builders (see physics1.json, cs4.json).
    """
    nid = (node_id or "").lower()
    return nid.endswith("_false") or nid.endswith("_hyp")


def evidence_score(
    session: SessionGraph, node_id: str, question: str,
) -> float:
    """Cheap relevance score for ranking candidate supports in the briefing.

    Combines lexical relevance to the question with a few structural
    penalties so the model gravitates to specific, on-topic evidence
    instead of hubs, contradicted, or misconception nodes.
    """
    node = session.nodes.get(node_id)
    if node is None:
        return 0.0
    score = float(lexical_overlap(node.text or "", question or ""))
    # Hub penalty: name suggests a high-level cluster node.
    nid = node.id.lower()
    if nid.endswith("_hub") or "_hub_" in nid or nid.endswith("hub"):
        score -= 0.30
    # Degree penalty: anything wired up to too many edges is a hub.
    degree = len(session.all_neighbors(node.id))
    if degree >= 6:
        score -= 0.15
    # Contradiction penalty: node is on either side of a contradict edge.
    for e in session.edges:
        if e.relation == "contradict" and node.id in (e.src, e.dst):
            score -= 0.40
            break
    # Misconception penalty: `_false` body text states the wrong view; the
    # model must NOT cite it as direct evidence for the truth. Demote so
    # the correct counterpart wins ranking. Contradiction-style prompts
    # can still pick it up — the demotion is moderate, not crushing.
    if is_misconception_node(node.id):
        score -= 0.20
    # Type-of-node nudges
    if node.node_type == NODE_CONCLUSION:
        score += 0.05
    elif node.node_type == NODE_NOTE:
        score += 0.02
    return round(score, 3)


def _ensure_evidence_grounding(
    session: SessionGraph, supports: List[str],
) -> Optional[str]:
    """Return error message if no transitive support is evidence."""
    for s in supports:
        node = session.nodes.get(s)
        if node is None:
            continue
        if node.node_type == NODE_EVIDENCE:
            return None
        if has_evidence_grounding(session, s):
            return None
    return (
        "ungrounded: at least one support (transitively) must be an evidence "
        "node. Use EXPAND_NODE first or include an evidence id in supports."
    )


def _do_plan(action: Action, session: SessionGraph) -> ActionResult:
    if get_plan_steps(session):
        return _fail(action, "plan already exists", "plan_already_set")
    raw_steps = action.args.get("steps")
    if not isinstance(raw_steps, list) or not raw_steps:
        return _fail(action, "steps must be a non-empty list", "bad_args")
    steps = [str(s).strip() for s in raw_steps if str(s).strip()]
    if not (2 <= len(steps) <= 6):
        return _fail(action, f"plan must have 2-6 steps (got {len(steps)})", "bad_plan_size")
    added = 0
    for i, text in enumerate(steps):
        pid = f"ps_{i + 1}"
        if pid in session.nodes:
            continue
        session.add_node(SessionNode(
            id=pid, text=text, node_type=NODE_PLAN_STEP,
            confidence=0.7, relevance=0.9,
            metadata={"order": i},
            created_step=session.step,
        ))
        session.add_edge(pid, "Q0", REL_PART_OF, status="verified", confidence=0.9)
        added += 1
    return _ok(action, f"plan written ({added} steps)", new_nodes=added, new_edges=added)


def _import_missing_edges(session: SessionGraph, graph: MemoryGraph) -> int:
    """Pull in any main-graph edges whose endpoints are BOTH now in the
    session but which haven't yet been added. This catches contradict /
    support edges that expand_node misses because they were INCOMING to
    the expanded node rather than outgoing.
    """
    added = 0
    for e in graph.edges:
        src = getattr(e, "src", None) or (e.get("src") if isinstance(e, dict) else None)
        dst = getattr(e, "dst", None) or (e.get("dst") if isinstance(e, dict) else None)
        rel = getattr(e, "relation", None) or (e.get("relation") if isinstance(e, dict) else None) or "related"
        if not src or not dst:
            continue
        if src not in session.nodes or dst not in session.nodes:
            continue
        if session.edge_between(src, dst) is not None:
            continue
        if src == "Q0" or dst == "Q0":
            continue
        session.add_edge(src, dst, rel, status="draft", confidence=0.7)
        added += 1
    return added


def _do_expand_node(action: Action, session: SessionGraph, graph: MemoryGraph) -> ActionResult:
    node_id = str(action.args.get("node_id", "")).strip()
    if not node_id or node_id not in session.nodes:
        return _fail(action, f"node {node_id!r} not in session", "node_not_found")
    if node_id not in graph.nodes:
        return _fail(action, f"node {node_id!r} not in main graph", "not_in_graph")
    n_added = expand_node(session, node_id, graph)
    # expand_node only follows outgoing edges; pick up any edges between
    # already-session-resident nodes that it missed (e.g. contradict edges
    # whose dst is the just-expanded node).
    n_extra_edges = _import_missing_edges(session, graph)
    _drop = lambda items: [
        f for f in items
        if not (f.item_type == "expand_node" and f.node_id == node_id)
    ]
    session.frontier = _drop(session.frontier)
    n_new_paths = 0
    if n_added > 0:
        new_paths = find_paths_bfs(session, "Q0", max_depth=PATH_DEPTH_LIMIT)
        for p in new_paths:
            if p.path_id not in session.paths:
                session.add_path(p)
        build_frontier_from_paths(session, new_paths)
        session.frontier = _drop(session.frontier)
        n_new_paths = len(new_paths)
    return _ok(
        action,
        f"expanded {node_id}: +{n_added} nodes, +{n_new_paths} paths, +{n_extra_edges} edges",
        new_nodes=n_added, new_paths=n_new_paths, new_edges=n_extra_edges,
    )


def _do_note_or_conclude(
    action: Action, session: SessionGraph, *, is_conclusion: bool,
) -> ActionResult:
    text = str(action.args.get("text", "")).strip()
    if not text:
        return _fail(action, "text must be a non-empty string", "empty_text")

    supports = action.args.get("supports", [])
    if not isinstance(supports, list):
        return _fail(action, "supports must be a list", "bad_args")
    clean_supports, err = _validate_supports(session, supports)
    if err:
        return _fail(action, err, "ungrounded" if "Q0" in err or "non-empty" in err else "bad_supports")

    err = _ensure_evidence_grounding(session, clean_supports)
    if err:
        return _fail(action, err, "ungrounded")

    # support_usage map: required for NOTE/CONCLUDE. Forces the model to
    # declare WHAT was extracted from each cited support. Downstream
    # usage_coverage metric will check that each usage string is actually
    # consistent with the support node's text.
    support_usage = action.args.get("support_usage", {})
    if not isinstance(support_usage, dict):
        return _fail(
            action,
            "support_usage must be an object mapping each support id to a "
            "short string describing what was extracted from that support",
            "bad_args",
        )
    clean_usage: Dict[str, str] = {}
    missing_usage = []
    for sid in clean_supports:
        u = support_usage.get(sid)
        if not (isinstance(u, str) and u.strip()):
            missing_usage.append(sid)
        else:
            clean_usage[sid] = u.strip()
    extra_keys = [k for k in support_usage.keys() if k not in set(clean_supports)]
    if missing_usage or extra_keys:
        parts = []
        if missing_usage:
            parts.append(f"missing usage for: {missing_usage}")
        if extra_keys:
            parts.append(f"unknown ids in support_usage: {extra_keys}")
        return _fail(
            action,
            "support_usage keys must exactly equal supports. " + "; ".join(parts),
            "support_usage_mismatch",
        )

    plan_step = str(action.args.get("plan_step", "")).strip()
    if is_conclusion and not plan_step:
        return _fail(action, "CONCLUDE requires plan_step", "missing_plan_step")
    if plan_step and (plan_step not in session.nodes or session.nodes[plan_step].node_type != NODE_PLAN_STEP):
        return _fail(action, f"plan_step {plan_step!r} not found", "bad_plan_step")

    kind = NODE_CONCLUSION if is_conclusion else NODE_NOTE
    prefix = "c" if is_conclusion else "n"
    nid = _next_seq_id(session, prefix)
    session.add_node(SessionNode(
        id=nid, text=text, node_type=kind,
        confidence=0.7 if is_conclusion else 0.6, relevance=0.8,
        metadata={
            "plan_step": plan_step or None,
            "support_usage": clean_usage,
        },
        created_step=session.step,
    ))
    new_edges = 0
    for s in clean_supports:
        session.add_edge(s, nid, REL_SUPPORTS, status="verified", confidence=0.7)
        new_edges += 1
    if plan_step:
        session.add_edge(nid, plan_step, REL_PART_OF, status="verified", confidence=0.9)
        new_edges += 1

    return _ok(
        action,
        f"wrote {kind} {nid} with {len(clean_supports)} supports"
        + (f" under {plan_step}" if plan_step else ""),
        new_nodes=1, new_edges=new_edges,
    )


def _do_verify_edge(action: Action, session: SessionGraph) -> ActionResult:
    src = str(action.args.get("src", "")).strip()
    dst = str(action.args.get("dst", "")).strip()
    if not src or not dst:
        return _fail(action, "src and dst required", "bad_args")
    # Try given direction; if not found, try the reversed direction so the
    # model doesn't have to remember which way the edge was originally
    # stored. Especially important for symmetric relations like contradict
    # (A contradicts B  <=>  B contradicts A) — the model should be able
    # to VERIFY_EDGE either way.
    if session.edge_between(src, dst):
        actual_src, actual_dst = src, dst
    elif session.edge_between(dst, src):
        actual_src, actual_dst = dst, src
    else:
        return _fail(action, f"edge {src}<->{dst} not found in either direction", "edge_not_found")
    session.update_edge_status(actual_src, actual_dst, "verified", confidence=0.85)
    return _ok(action, f"verified {actual_src}->{actual_dst}")


def _do_create_hypothesis(action: Action, session: SessionGraph) -> ActionResult:
    text = str(action.args.get("text", "")).strip()
    parent = str(action.args.get("parent", action.args.get("parent_node", ""))).strip()
    if not text:
        return _fail(action, "text required", "empty_text")
    hid = f"hyp_{uuid.uuid4().hex[:8]}"
    session.add_node(SessionNode(
        id=hid, text=text, node_type=NODE_HYPOTHESIS,
        confidence=0.4, relevance=0.5,
        metadata={"parent": parent or None},
        created_step=session.step,
    ))
    new_edges = 0
    if parent and parent in session.nodes:
        session.add_edge(parent, hid, "related", status="draft", confidence=0.4)
        new_edges = 1
    return _ok(action, f"created hypothesis {hid}", new_nodes=1, new_edges=new_edges)


def _do_run_pred_local(
    action: Action,
    session: SessionGraph,
    graph: MemoryGraph,
    pred_model: Any,
    pred_device: Optional[torch.device],
) -> ActionResult:
    if pred_model is None:
        return _fail(action, "no PRED model available in this run", "no_model")
    if pred_device is None:
        return _fail(action, "no PRED device available", "no_device")
    focus_nodes = action.args.get("focus_nodes", []) or []
    memory_ids = [nid for nid in focus_nodes if nid in graph.nodes]
    if not memory_ids:
        memory_ids = [
            n.source_memory_id for n in session.nodes.values()
            if n.node_type == NODE_EVIDENCE and n.source_memory_id
        ][:5]
    memory_ids = memory_ids[:8]
    n_added = run_pred_and_apply(session, pred_model, session.question, memory_ids, graph, pred_device)
    new_paths = find_paths_bfs(session, "Q0", max_depth=PATH_DEPTH_LIMIT)
    for p in new_paths:
        if p.path_id not in session.paths:
            session.add_path(p)
    build_frontier_from_paths(session, new_paths)
    return _ok(
        action,
        f"PRED proposed +{n_added} nodes, +{len(new_paths)} paths",
        new_nodes=n_added, new_paths=len(new_paths),
    )


def execute_action(
    action: Action,
    session: SessionGraph,
    graph: MemoryGraph,
    pred_model: Any = None,
    pred_device: Optional[torch.device] = None,
) -> ActionResult:
    atype = action.action_type
    if atype == ActionType.PLAN:
        return _do_plan(action, session)
    if atype == ActionType.EXPAND_NODE:
        return _do_expand_node(action, session, graph)
    if atype == ActionType.NOTE:
        return _do_note_or_conclude(action, session, is_conclusion=False)
    if atype == ActionType.CONCLUDE:
        return _do_note_or_conclude(action, session, is_conclusion=True)
    if atype == ActionType.VERIFY_EDGE:
        return _do_verify_edge(action, session)
    if atype == ActionType.CREATE_HYPOTHESIS:
        return _do_create_hypothesis(action, session)
    if atype == ActionType.RUN_PRED_LOCAL:
        return _do_run_pred_local(action, session, graph, pred_model, pred_device)
    # FINALIZE_ANSWER and STOP are handled by the loop, not here.
    return _fail(action, f"action {atype.value} not handled by execute_action", "not_implemented")


